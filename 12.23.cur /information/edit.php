	電磁學或稱電動力學或古典電動力學。之所以稱為古典，是因為它不包括現代的量子電動力學的內容。電動力學這樣一個術語使用並不是非常嚴格，有時它也用來指電磁學中去除了靜電學、靜磁學後剩下的部分，是指電磁學與力學結合的部分。這個部分處理電磁場對帶電粒子的力學影響。馬克士威方程組通過方程式統一電磁學，並且揭示出光作為電磁波的本質。</div>
